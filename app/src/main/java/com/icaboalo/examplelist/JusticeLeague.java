public class JusticeLeague
{
    private String name;
    private int image;

    public JusticeLeague(String name, int image)
    {
        this.name = name;
        this.image = image;
    }

    public String getName()
    {
        return name;
    }

    public int getImage()
    {
        return image;
    }
}
